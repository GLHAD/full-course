# CSV (Comma Separated Values)

# É um formato de arquivo que armazena dados em forma de tabela, onde cada linha 
# representa uma linha da tabela e as colunas são separadas por vírgulas.

# Ele é amplamente utilizado para transferir dados entre sistemas de diferentes 
# plataformas, como por exemplo, para importar ou exportar dados para uma planilha
# (Google Sheets, Excel, LibreOffice, Calc, etc.) ou para base de dados. 
# 
# Um arquivo CSV geralmente tem a extensão ".csv" e pode ser aberto em um editor
# de texto ou em uma planilha eletrônica. 

# Um exemplo de arquivo CSV pode ser:

# Nome, Idade, Endereço
# Adryel Otto, 28, "Av. Brasília, 4380"
# Aline Persona, 26, "Rua Alfredo Jaime Fellipe, 130"

# A primeira linha do arquivo define os nomes das colunas, enquanto as linhas seguintes
# contém os valores, separados por vírgulas.

# Regras simples do CSV:
#   1 - Separe os valores das colunas com um delimitador único (,)
#   2 - Cada registro deve estar em uma linha
#   3 - Não deixar linhas ou espaços sobrando
#   4 - Use o caractere de escape (") quando o delimitador aparecer no valor